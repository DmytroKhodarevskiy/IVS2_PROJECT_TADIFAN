import main

def Factorial(n):
    if n == 0:
        return 1
    else:
        return n * Factorial(n-1)

def SquareRoot(n):
    return n ** 0.5

def Power(n, p):
    return n ** p

def Plus(n, p):
    return n + p

def Minus(n, p):
    return n - p

def Multiply(n, p):
    return n * p

def Divide(n, p):
    return n / p


